class FlightData:

    def __init__(self, price, origin_city, origin_airport, destination_city, destination_airport, out_date, out_airline, out_flight, return_date, return_airline, return_flight):
        self.price = price
        self.origin_city = origin_city
        self.origin_airport = origin_airport
        self.destination_city = destination_city
        self.destination_airport = destination_airport
        self.out_date = out_date
        self.out_airline = out_airline
        self.out_flight = out_flight
        self.return_date = return_date
        self.return_airline = return_airline
        self.return_flight = return_flight
